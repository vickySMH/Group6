function showBikeRack()
{
    var checkBox = document.getElementById("bikeRack");
    var field = document.getElementById("bikeRackField");
    if(checkBox.checked == true)
    {
        field.style.display = "block";
    }
    else
    {
        field.style.display = "none";
    }
}

